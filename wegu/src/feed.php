<!-- pagination script -->
<script> 
    const items = document.querySelectorAll('.page-item.card');
    const itemsPerPage = 6; // Number of items per page
    let currentPage = 1;

    function displayItemsForPage(pageNumber) {
      const startIndex = (pageNumber - 1) * itemsPerPage;
      const endIndex = startIndex + itemsPerPage;

      items.forEach((item, index) => {
        if (index >= startIndex && index < endIndex) {
          item.style.display = 'block';
        } else {
          item.style.display = 'none';
        }
      });

      updatePaginationUI(pageNumber);
    }

    function updatePaginationUI(currentPage) {
      const paginationElement = document.getElementById('pagination');
      const totalPages = Math.ceil(items.length / itemsPerPage);

      let paginationHTML = '';
      for (let i = 1; i <= totalPages; i++) {
        paginationHTML += `<a href="#" onclick="displayItemsForPage(${i})" class="${i === currentPage ? 'active' : ''}">${i}</a>`;
      }

      paginationElement.innerHTML = paginationHTML;
    }

    // Display initial page
    displayItemsForPage(currentPage);
  </script>
